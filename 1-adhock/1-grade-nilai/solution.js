let inputNilai = 70

if (inputNilai > 79 && inputNilai <= 101) {
  console.log('A')
} else if (inputNilai > 64) {
  console.log('B')
} else if (inputNilai > 49) {
  console.log('C')
} else if (inputNilai > 34) {
  console.log('D')
} else if (inputNilai > -1) {
  console.log('E')
} else {
  console.log('Nilai Invalid')
}